			<div class="col-md tegel">
				<p>Elk jaar organiseert het CCO (Cultureel Centrum Oosterwijk) een fotowedstrijd. Deze wedstrijd staat open voor iedereen die wil deelnemen. Amateurfotografen kunnen zo vaak deelnemen als ze zelf willen. Foto's kunnen ingestuurd worden via deze website.</p>
				<p>De jury bestaat uit de negen leden van de cultuurraad, voorgezeten door onze schepen van cultuur, mevrouw El Ghalizi. Zij beoordelen uiteraard niet alleen de inhoud van de foto's, maar ook de technische en artistieke kwaliteiten: cadrage, belichting en originaliteit. </p>
				<p>Na de zomer worden alle ingezonden foto's tentoongesteld in de expositieruimte van het Cultureel Centrum. Uiteraard krijgen de foto's van de drie laureaten een centrale plaats in deze expositie.</p>
